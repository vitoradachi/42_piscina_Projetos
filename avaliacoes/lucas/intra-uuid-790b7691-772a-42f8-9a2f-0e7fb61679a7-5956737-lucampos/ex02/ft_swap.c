#include<stdio.h>
void	ft_swap(int *a, int *b)
{
	int	a_backup;

	a_backup = *a;
	*a = *b;
	*b = a_backup;
}
int main()
{	
	int a;
	int b;
	
	a = 23;
	b = 45;

	ft_swap(&a, &b);
	printf("%d %d" , a, b);
}
